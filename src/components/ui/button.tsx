import * as React from "react";
import { Slot } from "@radix-ui/react-slot";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-lg text-sm font-medium ring-offset-background transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/90 shadow-soft hover:shadow-md",
        destructive: "bg-destructive text-destructive-foreground hover:bg-destructive/90",
        outline: "border border-input bg-background hover:bg-accent hover:text-accent-foreground",
        secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost: "hover:bg-accent hover:text-accent-foreground",
        link: "text-primary underline-offset-4 hover:underline",
        // Auth theme buttons
        auth: "bg-auth-blue text-primary-foreground hover:bg-auth-blue/90 shadow-soft hover:shadow-glow",
        // Dashboard theme buttons
        dashboard: "bg-dashboard-navy text-primary-foreground hover:bg-dashboard-navy/90 shadow-soft",
        teal: "bg-dashboard-teal text-primary-foreground hover:bg-dashboard-teal/90 shadow-soft",
        // About theme buttons
        saffron: "bg-about-saffron text-foreground hover:bg-about-saffron/90 shadow-soft font-semibold",
        purple: "bg-about-purple text-primary-foreground hover:bg-about-purple/90 shadow-soft",
        // Events theme buttons
        events: "bg-events-accent text-primary-foreground hover:bg-events-secondary shadow-soft",
        // Detail theme buttons
        detail: "bg-detail-teal text-primary-foreground hover:bg-detail-teal/90 shadow-soft",
        // Announcements theme buttons
        announce: "bg-announce-accent text-primary-foreground hover:bg-announce-accent/90 shadow-soft",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3",
        lg: "h-12 rounded-xl px-8 text-base",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  },
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button";
    return <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props} />;
  },
);
Button.displayName = "Button";

export { Button, buttonVariants };
